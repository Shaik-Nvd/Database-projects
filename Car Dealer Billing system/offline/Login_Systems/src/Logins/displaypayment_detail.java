    public ArrayList<User3> user3List(){
        ArrayList<User3> paymentsList = new ArrayList<>();
        try{
             Class.forName("com.mysql.jdbc.Driver");
             Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/testdb","root","");
             String query="SELECT * FROM payment";
             Statement st= con.createStatement();
             ResultSet rs= st.executeQuery(query);
             User3 payment;
             while(rs.next()){
                 payment=new User3(rs.getString("tax"), rs.getString("subtotal"), rs.getString("total") );
                 paymentsList.add(payment);
                 
             }
        }
         catch(Exception e){
            System.out.println(e);}
        return paymentsList;
        
       }
    
     public void show_payment(){
        ArrayList<User3> list = user3List();
        DefaultTableModel model = (DefaultTableModel)jtabledisplay3.getModel();
        Object[] row= new Object[5];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).gettax();
            row[1]=list.get(i).getsubtotal();
            row[2]=list.get(i).gettotal();
            
            
            model.addRow(row);
        }
     }

